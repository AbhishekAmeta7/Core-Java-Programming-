public class TypeCasting3 {
    public static void main(String[] args) {
        char ch = 'P';
        int a = ch;
        long b = ch;
        float c = ch;
        double d = ch;
        System.out.println("char: "+ch);
        System.out.println("int: "+a);
        System.out.println("long: "+b);
        System.out.println("float: "+c);
        System.out.println("double: "+d);
    }
}
// char -> int
// char -> long
// char ->  float
// char -> double 