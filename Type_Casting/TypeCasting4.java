public class TypeCasting4 {
    public static void main(String[] args) {
        int a = 100;
        long l = a;
        float f = a;
        double d = a;
        System.out.println("int: "+a);
        System.out.println("long: "+l);
        System.out.println("float: "+f);
        System.out.println("double: "+d);
    }
}
// int -> long
//  int ->  float
//  int -> double