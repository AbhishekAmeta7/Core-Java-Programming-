class CollegeStudentDetail {
    String name;
    String fname;
    String mname;
    String graduation;
    String address;
    long contact;
    String groupName;
    int sem;
    CollegeStudentDetail(String name,String fname,String mname,String graduation,String address,long contact,String groupName,int sem){
        this.name = name;
        this.fname = fname;
        this.mname = mname;
        this.graduation = graduation;
        this.address= address;
        this.contact=contact;
        this.groupName = groupName;
        this.sem = sem;
    }
}
